let additionDemo = 5 + 5;
additionDemo += 5;
console.log("Addition: ", additionDemo);
// Output: Addition: 15

let subtractionDemo = 10 - 5;
subtractionDemo -= 2;
console.log("Subtraction: ", subtractionDemo);
// Output: Subtraction: 3

let multiplicationDemo = 5 * 5;
multiplicationDemo *= 2;
console.log("Multiplication: ", multiplicationDemo);
// Output: Multiplication: 50

let exponentiationDemo = 5 ** 2;
exponentiationDemo **= 2;
console.log("Exponentiation: ", exponentiationDemo);
// Output: Exponentiation: 625

let divisionDemo = 10 / 2;
divisionDemo /= 2;
console.log("Division: ", divisionDemo);
// Output: Division: 2.5

